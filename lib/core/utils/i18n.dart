  /// 暴露内部表的可变引用，供子 spec 注册键值与回归脚本读取。
  ///
  /// 注意：返回的是同一份引用，写入会直接生效。
  static Map<String, Map<String, String>> get raw => _raw;

  /// 按 [key] 查表；[lang] 缺省为 'zh'，命中失败回退到 'en'，再失败返回 [key] 本身。
  static String t(String key, {String? lang}) {
    final String preferred = lang ?? 'zh';
    final Map<String, String>? primary = _raw[preferred];
    if (primary != null) {
      final String? hit = primary[key];
      if (hit != null) return hit;
    }
    if (preferred != 'en') {
      final Map<String, String>? fallback = _raw['en'];
      if (fallback != null) {
        final String? hit = fallback[key];
        if (hit != null) return hit;
      }
    }
    return key;
  }

  /// 在 [t] 的基础上把模板中形如 `{name}` 的占位符替换为 [args] 的对应值。
  ///
  /// 行为契约（与 design.md §组件 2 对齐）：
  /// - [args] 为空时直接返回 `t(key, lang: lang)`，不进入正则扫描；
  /// - 占位符 `name` 必须匹配 `[A-Za-z_][A-Za-z0-9_]*`；
  /// - [args] 中存在键时，把 `{name}` 替换为对应值的 `toString()`；
  ///   若值为 `null` 则替换为空字符串 `''`；
  /// - [args] 中缺失键时，保留原始字面量 `{name}`（**不**抛异常，便于
  ///   调用方观察并补齐 args）；
  /// - [args] 中多余键允许存在，会被静默忽略；
  /// - 使用 `replaceAllMapped` 一次扫描完成，避免「先 a 再 b」的两步
  ///   替换破坏值本身已含 `{...}` 字面量的情况；
  /// - 不支持复数 / ICU 选择子句；与主项目 `t(...).replace(/\{name\}/g, value)`
  ///   等价语义。
  static String tArgs(String key, Map<String, Object?> args, {String? lang}) {
    final String result = t(key, lang: lang);
    if (args.isEmpty) return result;
    // FIX(i18n)：原正则 `\{([A-Za-z_][A-Za-z0-9_]*)\}` 会贪婪地把 `{{user}}` 内
    // 的 `{user}` 子串当成占位符匹配（最外层的 `{{`、`}}` 在 TypeScript 主项目
    // 里仅是字面量）。改用前后向断言把两侧紧邻另一个花括号的写法排除掉，
    // 这样 `{{user}}` 不会被误替换，单独的 `{name}` 才会进入替换分支。
    // Dart RegExp 默认支持 `(?<!...)` lookbehind 与 `(?!...)` lookahead，
    // 无需额外开启 unicode 标志。
    final RegExp pattern = RegExp(r'(?<!\{)\{([A-Za-z_][A-Za-z0-9_]*)\}(?!\})');
    return result.replaceAllMapped(pattern, (Match m) {
      final String name = m.group(1)!;
      if (!args.containsKey(name)) return m.group(0)!;
      final Object? value = args[name];
      return value?.toString() ?? '';
    });
  }

  /// 返回 zh / en 两组语言键集合的并集。
  ///
  /// 该方法是 Property 20（i18n 键集合差集为空）与 RC-6 的核心读取入口。
  static Set<String> allKeys() {
    final Set<String> result = <String>{};
    for (final Map<String, String> table in _raw.values) {
      result.addAll(table.keys);
    }
    return result;
  }
